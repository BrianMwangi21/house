function isMeteorInstalled() {
    if [[ ! $(which meteor;) ]]
    then
        echo "false";
    else
        echo "true";
    fi
}

function meteorInstall() {
    groupLog "meteorInstall";
    installed=$(isMeteorInstalled;);
    if [[ "${installed}" == "false" ]]
    then
        npm install -g meteor;
    fi
}
