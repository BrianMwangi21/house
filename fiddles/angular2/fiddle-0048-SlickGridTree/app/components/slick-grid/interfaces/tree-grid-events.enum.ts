export enum TreeGridEventsEnum {
  /**
   * The hidden columns have changed.
   */
  HiddenColumns,

  /**
   * The grid was sorted.
   */
  Sort,

  /**
   * The columns' state has changed.
   */
  ColumnsState,

  /**
   * A cell was clicked.
   */
  CellClick,

  /**
   * A row was double clicked.
   */
  RowDoubleClick,

  /**
   * A row was selected.
   */
  RowSelection
}
